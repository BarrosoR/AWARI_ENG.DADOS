#1 - Faça um programa que leia duas notas de um aluno e imprima na tela sua média.

#Inserindo as notas
nota1= float(input("Digite a primeira nota: "))
nota2= float(input("Digite a segunda nota: "))

#Calculo da media

media= (nota1+nota2)/2

print("A media desse estudante foi", media)



